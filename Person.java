public class Person {
    private String firstN;
    private String lastN;

    public void setFirstName(String firstN) {
        this.firstN = firstN;
    }
    public void setLastName(String lastN) {
        this.lastN = lastN;
    }

    public String getFirstName() {
        return firstN;
    }
    public String getLastName() {
        return lastN;
    }

}
